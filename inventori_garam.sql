CREATE TRIGGER `pengeluaran_ai` AFTER INSERT ON `pengeluaran` FOR EACH ROW
BEGIN
update garam_gudang a set a.stok= (a.stok- NEW.tonase), a.keluar = (a.keluar + NEW.tonase) where id_garam_gudang = NEW.garam_gudang_id;
insert into garam_mutasi (garam_gudang_id,tipe,jumlah,referensi_id, created_at) values (NEW.garam_gudang_id,'out',NEW.tonase, NEW.id_pengeluaran, NEW.tanggal);
END;;

CREATE TRIGGER `pengeluaran_au` AFTER UPDATE ON `pengeluaran` FOR EACH ROW
begin
update garam_gudang a set a.stok= (a.stok- NEW.tonase + OLD.tonase), a.keluar= (a.keluar+ NEW.tonase + OLD.tonase)  where id_garam_gudang = NEW.garam_gudang_id;
update garam_mutasi set jumlah= NEW.tonase , created_at = NEW.tanggal where referensi_id = OLD.id_pengeluaran and tipe like 'out';
end;;

CREATE TRIGGER `pengeluaran_ad` AFTER DELETE ON `pengeluaran` FOR EACH ROW
BEGIN

update garam_gudang a set a.stok= (a.stok+ OLD.tonase), a.keluar= (a.keluar - OLD.tonase) where id_garam_gudang = OLD.garam_gudang_id;
delete from garam_mutasi where tipe like 'out' and referensi_id = OLD.id_pengeluaran;
END;;

