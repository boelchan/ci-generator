BEGIN

update garam_gudang a set a.stok= (a.stok+ NEW.tonase), a.masuk= (a.masuk+ NEW.tonase) where id_garam_gudang = NEW.garam_gudang_id;
insert into garam_mutasi (garam_gudang_id,tipe,jumlah,referensi_id,created_at) values (NEW.garam_gudang_id,'in',NEW.tonase, NEW.id_pemasukan, NEW.tanggal);
END;;

CREATE TRIGGER `pemasukan_au` AFTER UPDATE ON `pemasukan` FOR EACH ROW
begin
update garam_gudang a set a.stok = (a.stok + NEW.tonase - OLD.tonase), a.masuk= (a.masuk + NEW.tonase - OLD.tonase) where id_garam_gudang = NEW.garam_gudang_id;
update garam_mutasi set jumlah= NEW.tonase, created_at = NEW.tanggal where referensi_id = OLD.id_pemasukan and tipe like 'in';
end;;

CREATE TRIGGER `pemasukan_ad` AFTER DELETE ON `pemasukan` FOR EACH ROW
BEGIN
update garam_gudang a set a.stok = (a.stok - OLD.tonase), a.masuk= (a.masuk- OLD.tonase) where id_garam_gudang = OLD.garam_gudang_id;
delete from garam_mutasi where tipe like 'in' and referensi_id = OLD.id_pemasukan;
END;;

